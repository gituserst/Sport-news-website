        <h2 class="featurette-heading">Organizatori: Australijan open će se održati prema planu <span class="text-muted">Neće biti odlaganja Australijan opena.</span></h2>
        <p class="lead">Organizatori Otvorenog prvenstva Australije u Melburnu saopštili su danas da će se taj teniski turnir
          održati prema planu, uprkos lošem kvalitetu vazduha zbog velikih požara koji su zahvatili tu zemlju.Pojedini igrači
          žalili su se zbog loših uslova, jer su morali da igraju kvalifikacije po magli i dimu.
          "Mnogo je spekulacija o Australijan openu, otkazivanju ili odlaganju turnira. Australijan open će se održati", rekao
          je direktor turnira Kreg Tili, preneo je Bi-Bi-Si (BBC).
          Slovenačka teniserka Dalila Jakupović igrala je u utorak u kvalifikacijama protiv Švajcarkinje Stefani Fegele, u jednom
          trenutku pala je na kolena i počela da kašlje. Imala je problema sa disanjem i morala je da preda meč, u kome je vodila
          sa 1:0 u setovima i bila je na poen od taj brejka u drugom setu.
          Britanac Lijam Broudi rekao je da je u prvom kolu kvalifikacija "morao da se bori za vazduh" i da mu je "krv ključala".
          On je izgubio od Belorusa Ilje Ivaška.
          Stanovnicima Melburna u utorak je savetovano da ostanu u kućama i da ne izvode kućne ljubimce.
          Brojni teniseri kritikovali su organizatore što nisu pomerili početak turnira.
          Tili je rekao da razume bes igrača i dodao da nastaje zabuna u pogledu vidjenja različitih merenja kvaliteta vazduha,
          u zavisnosti od aplikacije ili lokacije koje su koristili.
          "Kvalitet vazduha je vrlo složeno i zbunjujuće pitanje koje se odnosi na brojne različite faktore. Različiti sajtovi i
          aplikacije vam daju različita očitavanja. Ovde se radi o poverenju u lekarske savete i poverenju u stručnost i naučne
          savete ljudi koji svakodnevno obavljaju analize", rekao je Tinli.
          "Bezbednost i dobrobit igrača je prioritet, zajedno sa našim osobljem na turniru i navijačima", dodao je on.Kvalifikacije
           u utorak su bile odložene na sat vremena, pošto su očitavanja pokazala da je vazduh "vrlo nezdrav".
          Ako mečevi zbog lošeg kvaliteta vazduha budu prekinuti, odigraće se pod krovom u Rod Lejver areni, Margaret Kort areni i
           Melburn areni. Prvi grend slem turnir u sezoni, Australijan open, počinje u ponedeljak.
           Titule brane Novak Djoković i Naomi Osaka.</p>
