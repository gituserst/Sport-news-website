        <h2 class="featurette-heading">Trener Mornara ''nacrtao'' rasplet u ABA ligi, ova utakmica je Zvezdino ''biti il' ne biti''?! <span class="text-muted">Da li će se ostvariti ova njegova prognoza? Da nije malo preterao?</span></h2>
        <p class="lead">Ekipa Mornara ostvarila je juče veoma važnu pobedu u trci za plasman u plej-ofABA lige. Savladana je Cedevita Olimpija posle produžetka sa 91:90, pa su sada Barani na samo pobedu zaostatka za Ljubljančanima, a borbu za ulazak u četiri najbolje ekipe vodiće i sa Zvezdom i FMP-om.
            Ljubljančane je posle dramatičnog meča utešio željom da i jedni i drugi nađu svoje mesto u plej-ofu, a onda progrnozirao da bi meč između Mornara i Crvene zvezde u Baru mogao da bude odlučujući kada je u pitanju četvrta pozicija.
            ''Od sveg srca bih voleo da oba tima uđu u plej-of. Cedevita ima dobru poziciju, mi jurimo to mesto, zajedno sa Zvezdom i FMP-om. Sa ovom našom pobedom, ta borba će, sigurno, trajati do kraja. Kako se sve crta, najverovatnije će ta utakmica u Baru sa Zvezdom odlučiti o putniku u plej-of", istakao je Pavićević na konferenciji za medije, a prenose ''Vijesti''.
            Možda je Pavićević malo i preterao, ali verovatno očekuje i da Zvezda prethodno izgubi i od Partizana u derbiju, što bi olakšalo put Baranima u plej-of.
            Ipak, posle meča sa Zvezdom koji je na programu 10. februara, Barani će imati teško gostovanje u Podgorici protiv Budućnosti. Imaju li kvalitet da dobiju obe ove utakmice...?
            Potom je usledilo podsećanje jednog od prisutnih novinara da je Pavićević svojevremeno bio trener Crvene zvezde i da bi mu ''kao Zvezdašu'', bilo teško u tom ''odlučujućem meču'' sa Beograđanima.
            On se sa tom konstatacijom nije složio.
            "Ja sam bio trener Zvezde, ali pre toga sam bio trener Mornara. Da li znate da je Mornar nama sve, i gradu, a pogotovo našoj familiji, bratu i meni. S tim se ništa ne može meriti", objasnio je Pavićević.</p>
